      tecnológica
    </h3>
    <p>
      Bogotá, 25 de septiembre de 2023 Boletín No. 169 Sentencia T-356-23 El
      llamado obedece al estudio de tutela que presentó una mujer venezolana en
      favor de su hija, a quien se le negó la posibilidad de acceder al grado de
      bachiller, toda vez que Migración Colombia no hizo efectiva la entrega del
      Permiso de Protección Temporal (PPT), que exigía la institución académica
      para otorgar el título. accionante cuestionó a la entidad estatal y a la
      institución educativa, al considerar que se le vulneró el derecho al
      debido proceso y a la educación de la joven. En este orden, la mujer
      solicitó que se ordene a Migración Colombia expedir y entregar el PPT de
      su hija y al colegio expedir su diploma y acta de grado.
