    <h3>
      Corte convoca sesión técnica para verificar cumplimiento de órdenes en la
      Sentencia que amparó los derechos a tres comunidades étnicas que
      desarrollan sus actividades en el arroyo Bruno en La Guajira
    </h3>
    <p>
      Bogotá, 22 de septiembre de 2023 Boletín No. 168 La convocatoria obedece a
      una solicitud de seguimiento de las órdenes que impartió la Corte a través
      de la Sentencia SU 698 de 2017 en la que la Sala Plena amparó los derechos
      al agua, a la seguridad alimentaria y a la salud de las comunidades
      indígenas de La Horqueta, Gran Parada y Paradero de la etnia Wayúu, ante
      la amenaza ocasionada por la desviación parcial del cauce del arroyo Bruno
      propuesto por la empresa Carbones del Cerrejón Limited. En el curso del
      seguimiento se ha puesto de presente el posible incumplimiento a las
      órdenes cuarta, quinta, séptima, sexta y octava respecto de: fomentar
      espacios de participación para las comunidades y personal técnico;
      implementar un estudio técnico que ofrezca una respuesta a las
      incertidumbres e interrogantes de las comunidades; incorporar un Plan de
      Manejo Ambiental Integral a las conclusiones del estudio técnico realizado
      y decidir sobre el restablecimiento del paso de las aguas superficiales
      del Arroyo Bruno hacía su cauce natural.
